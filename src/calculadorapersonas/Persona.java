package calculadorapersonas;

public class Persona {
    
    String nombre;
    int edad;
    String provincia;
    int cantidad = 0;
    
    Persona(String nombre, int edad, String provincia) {
        this.nombre = nombre;
        this.edad = edad;
        this.provincia = provincia;
    }
    
    Persona() {
    cantidad = cantidad; 
    }
    
}
